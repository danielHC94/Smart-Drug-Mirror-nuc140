void Buzzer() // Buzzer (alarm) function
{
	//if alarm is set 00:00:00 = alarm off
	int8_t press,BTflag=0;
	if(TimerHour==TimerHourAlarm && TimerMin==TimerMinAlarm && TimerSec==TimerSecAlarm && AlarmOn==1)
		{
			TimeFlag = 0; //Disable timer display
			while (1)
			{
				if(BTflag==0)
					{
						DrvUART_Write(UART_PORT0, "Alarm\n" ,6); //Return (treminal print) to Phone text
						BTflag=1;
					}
								
			switch(press)
				{
				case 8:
					clr_all_panel();
					DrvSYS_Delay(500000);					// delay 
					MEDLOAD();
				  TimeFlag = 1; //Enable timer display
					return;
				
				default:
					draw_LCD(mark);
					press=Scankey();
					DrvSYS_Delay(5000);					// delay 
					if (strncmp (TEXT, "8",1)==0){ //Enter to case 8 by BT
					press=8;
					}	
					DrvGPIO_ClrBit(E_GPB,11); // GPB11 = 0 to turn on Buzzer
					DrvGPIO_ClrBit(E_GPA,14); // GPA14 = Red
					DrvSYS_Delay(100000);					// delay 
					DrvGPIO_SetBit(E_GPB,11); // GPB11 = 1 to turn off Buzzer
					DrvGPIO_SetBit(E_GPA,14); // GPA14 = Red
					DrvSYS_Delay(100000);					// delay 
				}
			}
		}
}


